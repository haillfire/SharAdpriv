
public class funcionario {
	private int id;
	private String nome;
	private int raking;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getRaking() {
		return raking;
	}
	public void setRaking(int raking) {
		this.raking = raking;
	}
	
	
}
